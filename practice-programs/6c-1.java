//High School app
public class Person //Parent Class
{
	String name;
	int age;
	String qualification;
	public Person()
	{
		name = "Steve Rogers";
		age = 45;
		qualification = "B.E";
	}
}

class Student//Child Class
{
	String name;
	int age;
	String School;
	public Student()
	{
		name = "Bucky Barnes";
		age  = 20;
		School = "Queens School";
	}
}
